-- 문제 11. (SQL 작성형) 재고가 있는 상품의 카테고리별 최고가 조회하기 (5점)
-- Product 테이블에는 상품의 카테고리(CategoryId), 가격(Price), 재고 수량(StockQuantity)이 저장되어 있습니다.
-- 재고가 남아 있는 상품들 중에서 카테고리별로 가장 비싼 상품의 가격을 파악하려고 합니다.
-- [요구사항]
--  StockQuantity가 0보다 큰 상품을 대상으로, 카테고리별 최고 가격을 조회하는 SQL문을 작성하시오.
--  결과 컬럼명은 CategoryId, max_price 로 표시하시오.
--  결과는 max_price 내림차순으로 정렬하시오.
-- 최종 제출용 쿼리만 남기고, 다른 쿼리는 모두 삭제한 뒤 제출하시오. (주석 불가)
-------------------------------- 지우지 마시오----------------------------------------------------------------------------------------------------
SELECT
	CategoryId, Price AS max_price
FROM
	Product
GROUP BY
	CategoryId
ON MAX
	CategoryId



