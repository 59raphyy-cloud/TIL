-- 문제 09. (SQL 작성형) 가격이 50 이상인 상품 조회하기 (10점)
-- Product 테이블에는 상품의 이름(Name)과 가격(Price) 정보가 저장되어 있습니다.
-- [요구사항]
-- Price가 50 이상인 상품의 Name과 Price를 조회하는 SQL문을 작성하시오.
-- 결과는 Price 내림차순으로 정렬하시오.
-- 최종 제출용 쿼리만 남기고, 다른 쿼리는 모두 삭제한 뒤 제출하시오. (주석 불가)
-------------------------------- 지우지 마시오----------------------------------------------------------------------------------------------------
SELECT
	Name, Price
FROM
	Product
WHERE
	Price >= 50
ORDER BY
	Price DESC;







