def find_second_largest(numbers):
    # 여기에 코드를 작성하여 함수를 완성합니다.
    # 중복을 제거하기 위해 set로 형변환한다.
    # 인덱싱을 위해 다시 리스트로 형변환하여 새로운 변수에 할당한다.
    new_list = list(set(numbers))
    sorted_numbers = sorted(numbers)
    return sorted_numbers[-2]

