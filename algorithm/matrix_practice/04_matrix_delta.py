# 델타 이동과 경계 체크
# 방향 벡터(dr, dc)를 정의하고 if문으로 범위를 체크하는 패턴을 암기 수준으로 익히기

# 3x3 배열
arr = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
]
N, M = 3, 3

# 기준점: 중앙 (5)
r, c = 1, 1

# ---------------------------------------------------------
# 1. 델타 배열 정의 (상, 하, 좌, 우)
# ---------------------------------------------------------
# [실습 1] 상, 하, 좌, 우 순서로 변화량을 리스트에 채우세요.
dr = [-1, 1, 0, 0]
dc = [0, 0, -1, 1]

print(f"기준 좌표: ({r}, {c}), 값: {arr[r][c]}")
print("--- 4방향 탐색 시작 ---")

# ---------------------------------------------------------
# 1-2. '회전'이나 '나선형' 문제 (상, 우, 하, 좌)
# ---------------------------------------------------------
# 인덱스 i를 (i + 1) % 4와 같이 모듈러 연산으로 처리하여 방향 전환 자동화
# 방향 전환: next_dir = (current_dir + 1) % 4


for i in range(4):
    # [실습 2] 현재 좌표(r,c)에 델타를 더해 다음 좌표(nr,nc)를 구하세요.
    # 다음 좌표 계산
    nr = r + dr[i]
    nc = c + dc[i]

    # -----------------------------------------------------
    # 2. 경계 체크 (Boundary Check) - 필수!
    # -----------------------------------------------------
    # [실습 3] 인덱스가 배열 범위를 벗어나지 않는지 확인하는 if문을 작성하세요.
    # 경계 체크 구문은 알고리즘 문제에서 매우 높은 빈도로 활용되므로 반드시 숙지
    if 0 <= nr < N and 0 <= nc < M:
        print(f"방향 {i}: ({nr}, {nc}) -> 값: {arr[nr][nc]}")
    else:
        print(f"방향 {i}: ({nr}, {nc}) -> 범위 벗어남!")


# ---------------------------------------------------------
# 3. 전치 행렬
# ---------------------------------------------------------
# 1) zip 활용
transposed_arr = list(map(list, zip(*arr)))

# 2) 반복문 활용
for r in range(N):
    for c in range(M):
        if r < c:
            arr[r][c], arr[c][r] = arr[c][r], arr[r][c]

# ---------------------------------------------------------
# 3-2. 오른쪽 90도 회전
# ---------------------------------------------------------
# 1) 행을 뒤집기
# 2) 전치하기
rotated_90_right = list(map(list, zip(*arr[::-1])))

# ---------------------------------------------------------
# 3-3. 왼쪽 90도 회전
# ---------------------------------------------------------
# 1) 전치하기
# 2) 행을 뒤집기
rotated_90_left = list(map(list, zip(*arr)))[::-1]

# ---------------------------------------------------------
# 3-4. 180도 회전
# ---------------------------------------------------------
# 1) 열을 뒤집기
# 2) 행을 뒤집기
# or
# 1) 90도 회전 2번 하기
rotated_180 = [list(row)[::-1] for row in arr[::-1]]