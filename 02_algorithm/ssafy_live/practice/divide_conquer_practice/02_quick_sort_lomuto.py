"""
퀵 정렬 (Quick Sort - Lomuto 파티션)
- 맨 뒤의 원소를 피벗(반장)으로 삼고, 피벗보다 작은 값은 왼쪽, 큰 값은 오른쪽으로 보내는 로무토 방식을 구현
"""


def partition(arr, start, end):
    """
    분할(Partition)을 담당하는 실무자 함수
    - 가장 오른쪽 원소(arr[end])를 피벗으로 설정
    - 피벗보다 작은 값들은 왼쪽으로, 큰 값들은 오른쪽으로 재배치
    - 최종적으로 피벗이 있어야 할 올바른 위치의 인덱스 값을 반환
    """

    # 1. 가장 오르쪽 원소를 피벗으로 설정
    # [실습 1] 배열의 맨 끝 요소를 피벗으로 설정하세요.
    pivot = arr[end]

    # i: swap할 위치
    # 피벗보다 작은 값들이 모이는 구역의 마지막 인덱스 (경계)
    i = start - 1

    # 2. start부터 end-1 까지 순회하며 피벗보다 작은 값 찾기
    # [실습 2] start부터 피벗 바로 앞(end-1)까지 순회하는 루프를 작성하세요.
    # j: 현재 탐색하고 있는 인덱스
    for j in range(start, end):
        # 만약 현재 값(arr[j])이 피벗보다 작다면?
        if arr[j] < pivot:
            # 피벗보다 작은 값을 찾았으므로, i 증가 (swap할 위치를 오른쪽으로 이동)
            i += 1

            # TODO: i번째 위치의 값과 j번째 위치의 값을 서로 교환(Swap)하세요.
            # 선택사항: i와 j의 위치가 다르면 교환 (같으면 교환하지 않음)
            if i != j:
                # i와 j의 위치를 교환하여 작은 원소를 왼쪽으로 보냄
                # 피벗보다 작은 값을 i 위치로 스왑
                # 경계(i)와 현재 원소의 위치(j)를 교환
                arr[i], arr[j] = arr[j], arr[i]

    # 3. 마지막으로 피벗을 작은 값 구역 바로 다음(i+1)과 스왑
    # 모든 순회가 끝나면, i+1 위치가 피벗이 들어갈 자리이기 때문
    # [실습 3] 피벗(arr[end])을 올바른 위치(i + 1)로 교환하세요.
    # TODO: i+1 위치의 값과 end 위치의 값을 스왑
    arr[i + 1], arr[end] = arr[end], arr[i + 1]

    # 피벗의 최종 인덱스를 반환
    return i + 1


def quick_sort(arr, start, end):
    """
    퀵 정렬을 재귀적으로 지시하는 '매니저' 함수
    """
    # 기저조건: 배열 길이가 1 이하(start >= end)이면 이미 정렬된 상태
    # 즉, 분할이 더 이상 불가능한 상태 (최소 단위)
    if start < end:
        # 1. 분할: partition 함수를 호출하여 피벗의 올바른 위치를 찾기
        # [실습 4] partition 함수를 호출하여 피벗의 인덱스를 구하세요.
        pivot_idx = partition(arr, start, end)

        # 2. 정복: 피벗을 제외한 왼쪽, 오른쪽 구간을 재귀적으로 정렬
        # [실습 5] 피벗을 기준으로 왼쪽 그룹과 오른쪽 그룹을 재귀적으로 정렬하세요.
        # 왼쪽 그룹 정렬
        quick_sort(arr, start, pivot_idx - 1)  # 피벗은 정렬이 끝났으므로 포함시키지 않는다(-1).
        # 오른쪽 그룹 정렬
        quick_sort(arr, pivot_idx + 1, end)


# --- 테스트 ---
data = [3, 2, 4, 6, 9, 1, 8, 7, 5]
print(f"정렬 전: {data}")  # [3, 2, 4, 6, 9, 1, 8, 7, 5]
quick_sort(data, 0, len(data) - 1)
print(f"정렬 후: {data}")  # [1, 2, 3, 4, 5, 6, 7, 8, 9]
